/**
 * Enum representing the different types of lessons available in the platform.
 */
public enum LessonType {
    /**
     * Video lesson - contains video content
     */
    VIDEO,

    /**
     * Article lesson - contains text/markdown content
     */
    ARTICLE,

    /**
     * Survey lesson - contains form/questionnaire content
     */
    SURVEY
}
